package modelo;

public class TuplaDouble {
	private Double x;
	private Double y; 
	
	public TuplaDouble(Double a, Double b){
		this.x = a;
		this.y = b;
	}


	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}
}